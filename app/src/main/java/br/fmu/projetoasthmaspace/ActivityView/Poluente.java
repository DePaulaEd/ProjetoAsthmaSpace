package br.fmu.projetoasthmaspace.ActivityView;

public class Poluente {
    public String sigla;
    public String nome;
    public String valor;
    public String status;

    public Poluente(String sigla, String nome, String valor, String status) {
        this.sigla = sigla;
        this.nome = nome;
        this.valor = valor;
        this.status = status;
    }
}
